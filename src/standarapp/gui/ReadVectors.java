    private void doButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doButtonActionPerformed
        // TODO add your handling code here:
        String nameIn = inFileTextField.getText();
        String nameOut = outFileTextField.getText();
        int number1 = Integer.valueOf(municipio_textField.getText());
        int number2 = Integer.valueOf(localidad_textField.getText());
        int number3 = Integer.valueOf(especie_textField.getText());
        int number4 = Integer.valueOf(fecha_textField.getText());
        int rowBegin = Integer.valueOf(fila_textField.getText());
      
        int[] col = {number1,number2,number3,number4};
               
        try {
            if (!nameIn.contains("...")) {
                if (nameOut.contains("...")) {
                    String answer = rr.lectureRegistry(nameIn, nameIn, col, 80, rowBegin);
                    answerLabel.setForeground(Color.BLUE);
                    answerLabel.setText(answer + nameIn);

                } else if (Lecture.determineExtensionFile(nameIn)) {
                    String answer = rr.lectureRegistry(nameIn, nameOut + "\\vectoresGenerados.xlsx", col, 80, rowBegin);
                    answerLabel.setForeground(Color.BLUE);
                    answerLabel.setText(answer + " en: " + nameOut + "\\vectoresGenerados.xlsx");
                } else {
                    String answer = rr.lectureRegistry(nameIn, nameOut + "\\vectoresGenerados.xls", col, 80, rowBegin);
                    answerLabel.setForeground(Color.BLUE);
                    answerLabel.setText(answer + " en: " + nameOut + "\\vectoresGenerados.xls");
                }
            } else {
                answerLabel.setForeground(Color.red);
                answerLabel.setText("No ha seleccionado ningun archivo");
            }
        } catch (Exception e) {
            answerLabel.setForeground(Color.red);
            answerLabel.setText("Revisar los campos de seleccion de archivos");
        }
    }//GEN-LAST:event_doButtonActionPerformed

